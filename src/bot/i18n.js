function normalizeLanguage(language) {
  return SUPPORTED_LANGUAGES.has(language) ? language : DEFAULT_LANGUAGE;
}

function translate(language, key, params = {}) {
  const lang = normalizeLanguage(language);
  const value = text[lang][key] ?? text[DEFAULT_LANGUAGE][key];
  return typeof value === 'function' ? value(params) : value;
}

async function getUserLanguage(userId) {
  const pref = await db.getUserPreference(userId).catch(() => null);
  return normalizeLanguage(pref?.language);
}

async function getInteractionLanguage(interaction) {
  return getUserLanguage(interaction.user.id);
}

async function setUserLanguage(userId, language) {
  const normalized = normalizeLanguage(language);
  await db.saveUserLanguage(userId, normalized);
  return normalized;
}

